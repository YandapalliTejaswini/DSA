public class ReplaceCharacters {
    public static void main(String[] args) {
        //Replace-replace(oldchar,newchar)
        String s="Tejaswini";
        System.out.println(s.replace('i','a'));
        String v="Tejaswini Reddy";
        System.out.println(v.replace("Reddy","Yandapalli"));
    }
}
